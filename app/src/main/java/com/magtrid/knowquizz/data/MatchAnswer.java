package com.magtrid.knowquizz.data;

public class MatchAnswer {
    private int edad;
    private int lover;

    public MatchAnswer(int edad, int lover) {
        this.edad = edad;
        this.lover = lover;
    }

    public int getEdad() {
        return edad;
    }

    public int getLover() {
        return lover;
    }


}
